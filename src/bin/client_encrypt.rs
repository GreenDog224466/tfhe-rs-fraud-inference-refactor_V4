use polars::prelude::*;
use std::fs::File;
use std::io::{BufWriter, Write}; 
use tfhe::zk::{CompactPkeCrs, ZkComputeLoad};
use tfhe::CompactPublicKey;
use zeroize::Zeroize; 

// --- FHE MATH CONSTANTS ---
const EXPECTED_FEATURES: usize = 433;

pub fn main() -> Result<(), Box<dyn std::error::Error>> {
    println!("--- [CLIENT] Initializing Web3 FHE Client ---");

    // --- 1. DYNAMIC METADATA (EXPLICIT PARAMS) ---
    // TODO(PROD): Inject via ethers-rs / alloy signer
    let sender_address: [u8; 20] = [0u8; 20]; 
    let nonce: u64 = 42;                      
    let chain_id: u64 = 9001;                 
    let block_expiry: u64 = 15_000_000;       

    let mut metadata = Vec::new();
    metadata.extend_from_slice(&sender_address);
    metadata.extend_from_slice(&nonce.to_be_bytes());
    metadata.extend_from_slice(&chain_id.to_be_bytes());
    metadata.extend_from_slice(&block_expiry.to_be_bytes()); // Binds proof to a specific timeframe
    
    // --- 2. LOAD NETWORK PARAMETERS ---
    println!("Loading Network Public Key & CRS...");
    let pk_file = std::fs::File::open("public_key.bin")?;
    let public_key: CompactPublicKey = bincode::deserialize_from(pk_file)?;
    eprintln!("DEBUG: public key loaded"); 

    // PRODUCTION NOTE: Using unversioned alias for prototype convenience.
    // For production, pin to a versioned parameter set:
    // tfhe::shortint::parameters::v1_0::V1_0_PARAM_MESSAGE_2_CARRY_2_KS_PBS_TUNIFORM_2M64
    // This guarantees compatibility across tfhe-rs version upgrades.
    let params = tfhe::shortint::parameters::PARAM_MESSAGE_2_CARRY_2_KS_PBS_TUNIFORM_2M64;
    let config = tfhe::ConfigBuilder::with_custom_parameters(params).build();
    eprintln!("DEBUG: config built"); 
    
    // MOCK ONLY: In production, the CRS and public network key are generated by
    // a threshold key generation (DKG) protocol — e.g. Zama's TKMS — where a
    // committee of operators jointly produce the keys without any single party
    // learning the secret. Clients fetch the resulting public artifacts via the
    // fhEVM RPC or a pre-packaged wallet SDK. Local generation here keeps the
    // mock self-contained.
    let crs = tfhe::zk::CompactPkeCrs::from_config(config, EXPECTED_FEATURES)?;
    use tfhe::zk::Compressible;
    let mut crs_file = BufWriter::new(File::create("crs.bin")?);
    let compressed_params = crs.public_params().compress();
    tfhe::safe_serialization::safe_serialize(&compressed_params, &mut crs_file, 1 << 30)?;
    crs_file.flush()?;
    eprintln!("DEBUG: CRS generated");   

    // --- 3. LOAD PROCESS DATA VIA POLARS ---
    let file_path = std::env::var("FEATURES_PATH")
        .unwrap_or_else(|_| "data/processed/scaled_features.parquet".to_string());
    println!("Loading scaled features from {}...", file_path);
    
    let mut file = std::fs::File::open(&file_path)?;
    let df = polars::prelude::ParquetReader::new(&mut file).finish()?;

    // Encrypt only the first transaction row; upstream file may contain many.
    let df = df.head(Some(1));

    if df.height() != 1 {
        return Err(format!("Expected exactly 1 transaction row, got {}. Batch mode not supported.", df.height()).into());
    }

    if df.width() != EXPECTED_FEATURES {
        return Err(format!("Expected exactly {} feature columns, got {}. Strip ID/label columns upstream.", EXPECTED_FEATURES, df.width()).into());
    }

    // --- 4. COMPUTE ZK-SNARK & ENCRYPT ---
    println!("Extracting data, enforcing bounds, and loading into ZK Builder...");
    let mut builder = tfhe::ProvenCompactCiphertextList::builder(&public_key);
    let column_names = df.get_column_names();

    // Buffer allocated to hold plaintexts strictly for encryption, then scrubbed
    let mut plaintext_buffer: Vec<i32> = Vec::with_capacity(EXPECTED_FEATURES);

    for j in 0..EXPECTED_FEATURES {
        let col_name = column_names[j];
        
        let val_i64: i64 = df.column(col_name)?
            .i64()?
            .get(0)
            .ok_or_else(|| format!("FATAL: Null value found in feature column '{}'", col_name))?;
        
        // THE CRYPTOGRAPHIC BOUNDS CHECK 
        if val_i64 < -8192 || val_i64 > 8192 {
            return Err(format!("FATAL: Feature '{}' out of bounds: {}. Normalisation Failed.", col_name, val_i64).into());
        }
        
        let val_i32 = val_i64 as i32;
        plaintext_buffer.push(val_i32);
        builder.push(val_i32);
    }

    println!("Computing ZK-SNARK Proof of Ciphertext Knowledge (PoCK)...");
    let proven_compact_list = builder.build_with_proof_packed(crs.public_params(), &metadata, ZkComputeLoad::Verify)?;

    // Cryptographic zeroization. This explicitly overwrites the RAM with 0s.
    // Zeroizes our plaintext copy in RAM before deallocation. Note: the builder's
    // internal copy and the Polars Arrow-allocated DataFrame are not scrubbed by this —
    // full coverage would require a custom allocator or avoiding Polars entirely.
    // Acceptable tradeoff for a standard Web3 client context.
    //
    // In this mock, zeroization isn't strictly necessary — the client key and source
    // parquet both persist on disk anyway. It's included because in a production Web3
    // client, scrubbing plaintext from memory is necessary hygiene against memory
    // disclosure (core dumps, swapped pages, freed-memory reads).
    plaintext_buffer.zeroize(); 

    // --- 5. SAVE FOR THE NETWORK ---
    let mut data_file = BufWriter::new(File::create("request.bin")?);
    bincode::serialize_into(&mut data_file, &proven_compact_list)?;
    data_file.flush()?;
    
    println!("--- [CLIENT] Success! ZK-SNARK 'request.bin' is ready for the mempool. ---");
    Ok(())
}